(function() {

  var $menuNav = $('.menu.nav');

  var $btnNavBar = $('.btn.btn-navbar').on('click', function() {    
    $menuNav.css('display', $menuNav.is(':visible') ? '' : 'block');
  });

  var timer;
  $(document).on('touchstart', function(e) {
    if (timer) return;
    timer = setTimeout(function() {
      timer = null;

      var $target = $(e.target);
      if ($btnNavBar.is(':visible') && $menuNav.is(':visible') && $target.closest('.btn-navbar').size() < 1 && $target.closest('.nav').size() < 1) {
        $menuNav.css('display', '');
      }
    }, 100);
  });

}());


(function() {

  var browser   = $.browser || {}
    , progress  = NProgress

  if (browser.msie && $.browser.versionNumber < 8) {
    return
  }

  progress.configure({
      parent    : '.navbar .navbar-inner'
    , template  : '<div class="bar" role="bar"></div>'
  })

  progress.start()

  $(document)
    .ready(function() {
      progress.inc(0.5)
      setTimeout(function() {
        progress.done()
      }, 4000)
    })
    .ajaxStart(function() {
      progress.start()
    })
    .ajaxComplete(function() {
      progress.done()
    })

  $(window).load(function() {
    progress.done()
  })

}());